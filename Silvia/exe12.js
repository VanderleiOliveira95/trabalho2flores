// O "for" repete um bloco de código várias vezes
// Estrutura: for (início; condição; incremento)
 
for (let i = 1; i <= 10; i++) {
 
  // A cada repetição, "i" aumenta 1
  // Calculamos 5 multiplicado pelo valor atual de "i"
  let resultado = 5 * i;
 
  // imprime a linha da tabuada 
  console.log("5 x " + i + " = " + resultado);
 
}